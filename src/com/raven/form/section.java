    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        dbclass dbclass=new dbclass();
        ResultSet rs = dbclass.sreachbatch(search.getText());
        if(search.getText().equals("Enter Batch Name") || search.getText().equals("")){
            rsearch.setText("*required");
        }
        else{
            rsearch.setText("");
            try {
                if(rs.isBeforeFirst()==true){

                    
                     ResultSet rs0= dbclass.sreachbatch(search.getText());
                     rs0.next();
                     String chkName=rs0.getString("batch");
                     
                      ResultSet rs1 = dbclass.sreachbatch(bname.getText());
                      boolean chkSecName=rs1.isBeforeFirst();
                     if(chkSecName==true && bname.getText().equals(chkName)==false)
                     {
                      JOptionPane.showMessageDialog(null,"dulicate entry "+bname.getText()+ " is Already Found in Database ","dulicate entry",JOptionPane.WARNING_MESSAGE);
                     }
                     else{
                    dbclass.updatebatch(semester.getSelectedItem().toString(),bname.getText(),search.getText(),tload.getSelectedItem().toString(),shift.getSelectedItem().toString());
                    JOptionPane.showMessageDialog(null,"Section data succesfully updated","update Section ",JOptionPane.INFORMATION_MESSAGE);
                    DefaultTableModel tbmodel=(DefaultTableModel)jTable1.getModel();
                    tbmodel.setRowCount(0);
                    showrecord();

                   }
                }
                else{
                    JOptionPane.showMessageDialog(null,"record not found","record not found",JOptionPane.ERROR_MESSAGE);
                }   } catch (SQLException ex) {
                   System.out.println(ex);
                }

            }
    }//GEN-LAST:event_jButton6ActionPerformed

