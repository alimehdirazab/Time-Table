    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if(bname.getText().equals("Enter Batch Name") || bname.getText().equals("") )

        {
            if(bname.getText().equals("Enter Batch Name") || bname.getText().equals("")){
                rname.setText("*required");
            }
            else{
                rname.setText("");
            }

        }
        else{

           dbclass insert=new  dbclass();
             ResultSet rs = insert.sreachbatch(bname.getText());
             
            try {
                if(rs.isBeforeFirst()==true){
                     JOptionPane.showMessageDialog(null,"dulicate entry "+bname.getText()+ " is Already Found in Database ","dulicate entry",JOptionPane.WARNING_MESSAGE);
                   
                }
                else
                {
            insert.insertbatch(semester.getSelectedItem().toString(),bname.getText(),tload.getSelectedItem().toString(),shift.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null,"Section succesfully added","Section add",JOptionPane.INFORMATION_MESSAGE);
            DefaultTableModel tbmodel=(DefaultTableModel)jTable1.getModel();
            tbmodel.setRowCount(0);
            showrecord();

            bname.setText("Enter Batch Name");
            bname.setForeground(new Color(153,153,153));
            rname.setText("");
            }
            }
            catch (SQLException ex) {
                System.out.println(ex);
            
        }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

